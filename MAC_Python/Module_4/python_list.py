# Introduction to python list

myList = ['apple', 'banana', 'mango']
print(myList)
print(myList[0:2]) # ['apple', 'banana']
    

number = [3,5,6,8,9]
print(number[1])
# get length of list
print(len(number))

# combine two list
print(myList + number) # ['apple', 'banana', 'mango', 3, 5, 6, 8, 9]






'''
Data Type
Numbers -> Double ->String ->  Boolean 
'''

# Number
print(1)
print(15 + 5)

# Double
print(12.8)
print(12.4 + 3.5)


# String
# String is a sequence of characters, either as a literal constant or as some kind of variable.
print('Hello' + ' ' + 'Class') # concatenation


# Boolean: True, False
print(True)
print(False)
